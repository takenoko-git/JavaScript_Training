let arr = ["b", "c", "d"];

// 配列の最後に要素を追加する（戻り値は変更後の配列の長さ）
console.log(arr.push("e"));
console.log(arr);

// 最後の要素を削除する（戻り値は削除された要素）
console.log(arr.pop());
console.log(arr);

// 先頭に要素を追加する（戻り値は変更後の配列の長さ）
console.log(arr.unshift("a"));
console.log(arr);

// 先頭の要素を削除する（戻り値は削除された要素）
console.log(arr.shift());
console.log(arr);
